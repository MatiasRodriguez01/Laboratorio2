package PolimorfismoYDependencias.Parametrico;

public class Persona {
    // attributes;
    private String nombre;

    // Constructor

    public Persona(String nombre) {
        this.nombre = nombre;
    }

    // Getters and setters

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
